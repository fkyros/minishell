#include "libft.h"
#include <stddef.h>
#include <stdlib.h>

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*tmp;
	void	*new_content;

	head = NULL;
	while (lst)
	{
		new_content = (*f)(lst->content);
		tmp = ft_lstnew(new_content);
		if (!tmp)
		{
			(*del)(new_content);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, tmp);
		lst = lst -> next;
	}
	return (head);
}
